public class MathTest{
     
     public static void main(String[] args){
     
         System.out.println("SQRT: " + Math.sqrt(39));
         
         System.out.println("SIN: " + Math.sin(Math.toRadians(270)));
         System.out.println("COS: " + Math.sin(Math.toRadians(270)));
         
         System.out.println("FLOOR: " + Math.floor(23));
         System.out.println("CEIL: " + Math.ceil(23));

         System.out.println("ROUND: " + Math.round(20.5));
         
         System.out.println("MAX: " + Math.max(70,80));
         System.out.println("MIN: " + Math.min(70,80));
         

         System.out.println("RANDOM: " + (Math.random()* 100));
        
     }
}